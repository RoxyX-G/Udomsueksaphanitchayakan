#include <stdio.h>
#include <conio.h>

int main() {
    int pen;
    int price_pen = 10;
    int book;
    int price_book = 200;
    int total_pen;
    int total_book;

    printf("Enter your Pen = ");
    scanf("%d", &pen);

    printf("Enter your Book = ");
    scanf("%d", &book);

    total_pen = pen * price_pen;
    printf("Total Pen = %d \n", total_pen);

    total_book = book * total_book;
    printf("Total Book = %d", total_book);

    return 0;
}

