#include <stdio.h>
int main() {
    int salary, hour, totalot, totalsalary;
    
    printf("Input Salary : ");
    scanf("%d", &salary);
    
    printf("Input Hour : ");
    scanf("%d", &hour);
    
    totalot = hour * 200;
    totalsalary = salary + totalot;
    
    printf("TotalSalary = %d", totalsalary);
    
    return 0;
}

